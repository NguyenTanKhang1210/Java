
public class LoaispObj {
	String maloai, tenloai;

	public LoaispObj()
	{
		
	}
	public LoaispObj(String maloai, String tenloaij) {
		super();
		this.maloai = maloai;
		this.tenloai = tenloaij;
	}
	public String getMaloai() {
		return maloai;
	}

	public void setMaloai(String maloai) {
		this.maloai = maloai;
	}

	public String getTenloai() {
		return tenloai;
	}

	public void setTenloai(String tenloai) {
		this.tenloai = tenloai;
	}


	

}
